# Calculate durations from original timecodes
def parse_tc(tc_str):
    start, end = tc_str.split('-')
    sh, sm = int(start.split(':')[0]), int(start.split(':')[1])
    eh, em = int(end.split(':')[0]), int(end.split(':')[1])
    return eh*60+em - (sh*60+sm)

upper_durs = [parse_tc(s['tc']) for s in upper]
lower_durs = [parse_tc(s['tc']) for s in lower]

print(f"Upper: {len(upper)} shots, {sum(upper_durs)}s")
print(f"Lower: {len(lower)} shots, {sum(lower_durs)}s")

def tc_fmt(s):
    return f'{int(s//60):02d}:{int(s%60):02d}'

def build_tcs(durations):
    tcs, cumsum = [], 0
    for d in durations:
        tcs.append(f'{tc_fmt(cumsum)}-{tc_fmt(cumsum+d)}')
        cumsum += d
    return tcs

upper_tcs = build_tcs(upper_durs)
lower_tcs = build_tcs(lower_durs)

